public class Lab066 {
    public static void main(String[] args) {
        //Interview Question
        boolean b = !true;
        //while assigning we are using an relational operator
        if (b) { //we can give the result of a condition also in the if condition
            System.out.println("b is true");
        }
        else {
            System.out.println("b is false");
        }

    }
}
