package September.Ex_16092024;

public class Lab030_UnaryOperator {
    public static void main(String[] args) {
        //Unary Operator
        int a = +65; //+ is greyed out as even if is not explicitly mentioned
        // it works the same way
        int b = -1; //- is a unary operator

        System.out.println(a);
        System.out.println(b);

        b = b+1;
        System.out.println(b);
    }
}
