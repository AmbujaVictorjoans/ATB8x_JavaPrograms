package September.Ex_16092024;

public class Lab028_ArithmeticOperator {
    public static void main(String[] args) {
        //Arithmetic Operators
        // +, -, *, /, %
        //They operate with more than one operands to operate

        int a = 28;
        int b = 9;

        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);

        //Modulus Operator "%" fetches the remainder after division
        System.out.println(a%b);

        System.out.println(67%13);

    }
}
