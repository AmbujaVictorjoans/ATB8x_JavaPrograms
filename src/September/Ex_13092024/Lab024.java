package September.Ex_13092024;

public class Lab024 {
    public static void main(String[] args) {
        //Constants - Fixed Value
        //Constants are variables
        //whose values cannot be changed once assigned
        //Constants are associated with the Keyword 'final'
        //syntax for Constant declaration is final datatype CONSTANT_NAME = value;
        //CONSTANT_NAME should always be in Capital letters;

        final float PIE = 3.1456f;
        //PIE = 12.456 - value of PIE cannot be changed,
        // once assigned value is constant throughout the program
        System.out.println(PIE);
    }
}
