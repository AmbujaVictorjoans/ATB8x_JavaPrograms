package September.Ex_23092024_Switch;

public class LAb_083 {
    public static void main(String[] args) {
        //Interview question

        char code = 'C';
        switch (code) {
            default:
                System.out.println("Hellooo");
                break;
            case 'A':
                System.out.println("65");
                break;
            case 'B':
                System.out.println("66");
                break;
            case 'C':
                System.out.println("67");
                break;
        }
    }
}
